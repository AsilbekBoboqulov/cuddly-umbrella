#include <iostream>
using namespace std;
int main()
{
    int a = 10;
    cout << a-- << endl;
    cout << a << endl;

    int b = 20;
    cout << b++ << endl;
    cout << b << endl;

    int c = 30;
    cout << ++c << endl;
    cout << c++ << endl;

    int d = 40;
    cout << --d << endl;
    cout << d-- << endl;
}
